				</li>

				<li>
					<div class="symbol-marked-list__title">Положительная оценка</div>

					<p>«+1» означает «все очень хорошо, очень редко бывают исключительно мелкие проблемки, которые быстро и&nbsp;правильно устраняются»; Оценку «+1» следует ставить в&nbsp;тех случаях, когда оцениваемый <span class="nobr">сотрудник/подразделение</span> выполняют свою работу на&nbsp;очень высоком уровне, качество прекрасное, взаимодействие с&nbsp;сотрудниками осуществляется легко, любые запросы/услуги обрабатываются быстро, происходит информирование о&nbsp;статусе выполнения заявки, сроках исполнения и&nbsp;пр.</p>
				</li>

				<li>
					<div class="symbol-marked-list__title">Нейтральная оценка</div>

					<p>«0» означает «работа ведется в&nbsp;текущем режиме, все нормально, периодически возникают проблемы среднего уровня значимости, которые решаются достаточно оперативно, не&nbsp;затягиваются и&nbsp;без привлечения вышестоящего руководства».</p>
