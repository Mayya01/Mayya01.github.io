			<h2 class="working-process__h2 module-h2">Формат SMART</h2>

			<p class="lead">У&nbsp;нас широко применяется формат SMART при решении задач.<br> Все руководители и&nbsp;многие сотрудники успешно используют его, и&nbsp;значительно облегчают себе достижение результата. Каждая задача в&nbsp;этом формате обязательно соответствует 5&nbsp;характеристикам:</p>

			<ul class="symbol-marked-list symbol-marked-list--smart-letters">
				<li>
					<div class="symbol-marked-list__title">Specific&nbsp;— конкретная</div>

					<p>Цель/задача должна быть недвусмысленной и&nbsp;однозначной, то&nbsp;есть сформулированной таким образом, чтобы у&nbsp;сотрудника не&nbsp;было ни&nbsp;одного шанса понять её&nbsp;не&nbsp;так или неправильно.</p>
				</li>

				<li>
					<div class="symbol-marked-list__title">Measure&nbsp;— измеримая</div>

					<p>Необходимо четко сформулировать критерии, по&nbsp;которым будет оценено&nbsp;&mdash; достигнута цель или нет. Эти критерии могут быть как количественными (в&nbsp;процентах, рублях etc), так и&nbsp;качественными.</p>
				</li>

				<li>
					<div class="symbol-marked-list__title">Achievable&nbsp;— осуществимая</div>

					<p>Завышенная цель демотивирует: если сотрудник считает цель невыполнимой, то&nbsp;вероятнее всего он&nbsp;не&nbsp;будет прилагать достаточных усилий для её&nbsp;выполнения.</p>
				</li>

				<li>
					<div class="symbol-marked-list__title">Relevant&nbsp;— релевантная</div>

					<p>Относится к&nbsp;большой цели, является ее&nbsp;частью. Сотрудник должен помнить, как реализация данной <span class="nobr">цели/задачи</span> способствует достижению стратегических планов и&nbsp;реализации миссии компании.</p>
				</li>

				<li>
					<div class="symbol-marked-list__title">Time Bounded&nbsp;— ограниченная по&nbsp;времени</div>

					<p>Ограниченность по&nbsp;времени, т.е. имеющая конечный срок.</p>
				</li>
			</ul>
		</div>
	</div>
</section>

